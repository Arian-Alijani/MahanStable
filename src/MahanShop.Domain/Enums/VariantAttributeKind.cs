namespace MahanShop.Domain.Enums;

/// <summary>
/// نقش یک ویژگی متغیر در رابط کاربری. صفحهٔ محصول بر اساس همین نقش‌ها بین دو جریان
/// «مبتنی بر دستگاه» (برند→مدل[→رنگ]) و «تنوع ساده» (رنگ/سایز/مدل) سوییچ می‌کند.
/// </summary>
public enum VariantAttributeKind
{
    /// <summary>ویژگی عمومی (سایز، نوع اتصال، …) — به‌صورت دراپ‌داون/دکمه نمایش داده می‌شود.</summary>
    Other = 0,

    /// <summary>برند دستگاه (اپل، سامسونگ، …). فعال‌بودن این نقش = صفحه به جریان «مبتنی بر دستگاه» می‌رود.</summary>
    Brand = 1,

    /// <summary>مدل دستگاه (iPhone 13، Galaxy A31، …). در جریان دستگاه زیرِ برند گروه می‌شود.</summary>
    Model = 2,

    /// <summary>رنگ — با swatch دایره‌ای رنگی نمایش داده می‌شود (ColorHex).</summary>
    Color = 3
}
